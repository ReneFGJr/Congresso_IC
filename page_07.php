						<TD align="left"> Curitiba é um município brasileiro, capital do estado do Paraná, localizada a 934 metros de altitude no primeiro planalto paranaense, a aproximadamente 110 quilômetros do Oceano Atlântico. É a oitava cidade mais populosa do Brasil e a maior do sul do país, com uma população de 1.850.000 habitantes. É a cidade principal da Região Metropolitana de Curitiba, formada por 29 municípios e que possui 3.400.000 habitantes sobre uma área de 15.447 km², o que a torna a oitava região metropolitana mais populosa do Brasil, e a maior da Região Sul, tendo ultrapassado recentemente a Região Metropolitana de Porto Alegre. A capital do Paraná ao longo dos últimos anos tem se consolidado como a cidade mais rica do Sul do país e a quarta em ambino nacional. (fonte:Wikipédia) </TD>
						<TD align="right">Curitiba is a Brazilian city, capital of the state of Paraná, located at 934 meters altitude in the first plateau of Paraná, approximately 110 km from the Atlantic Ocean. It is the eighth most populous of Brazil and the largest city in the south of the country, with a population of 1,850,000 inhabitants. It is the principal city of the metropolitan region of Curitiba, formed by 29 municipalities and has 3,400,000 inhabitants over an area of ​​15,447 km ², making it the eighth most populous metropolitan area in Brazil, and the largest in the South, having recently overtaken the Metropolitan Region of Porto Alegre. The capital of Parana over the past few years has been established as the richest city in the South and fourth in national ambino. (source: Wikipedia)</TD>
