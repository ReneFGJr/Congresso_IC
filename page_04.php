							<BR>
							<b>INDICAÇÕES GERAIS</b>
							<BR>
							<BR>
							<P>
								Todos os trabalhos devem ser inéditos e não podem estar submetidos à avaliação simultânea em outros periódicos.
								<BR>
								Afirmações, opiniões e conceitos expressados nos artigos são de responsabilidade dos autores.
								<BR>
								Todos os artigos serão submetidos à Comissão Editorial da Revista e para avaliação por pares externos à Instituição (<i>peer review</i>).
								<BR>
								Os trabalhos podem ser encaminhados em português, inglês, francês ou espanhol e o resumo deve ser apresentado na língua original do texto com a tradução para a língua inglesa e espanhola.
								<BR>
								Abreviações oficiais poderão ser empregadas somente após uma primeira menção completa.
								<BR>
								Deverão constar nos trabalhos: nome dos autores, titulação, filiação institucional, e-mail; endereço completo (não divulgado na publicação do artigo), para encaminhamento de correspondência pela Comissão Editorial. O preparo e encaminhamento com anonimato para avaliação é feito pelo editor e Comissão Editorial
								<BR>
								Não serão publicadas fotos coloridas, a não ser em caso de absoluta necessidade e a critério da Comissão Editorial.
								<BR>
								Os artigos recebidos são encaminhados a dois revisores (pareceristas) para avaliação pelos pares (<i>peer review</i>). Os editores coordenam as informações entre os autores e revisores, cabendo-lhes a decisão final sobre quais artigos serão publicados com base nas recomendações feitas pelos revisores. Quando recusados, os artigos serão devolvidos com a justificativa do editor. Caso aceitos, os textos passam pela revisão, editoração e publicação. Poderão ser reenviados os textos reformulados, quando for o caso.
								<BR>
								São observados os seguintes critérios: relevância para Comissão e para a área de educação; consistência e rigor teórico conceitual; articulação entre objetivo, fundamentação teórico metodológica, análise e conclusão de dados; bibliografia pertinente e atualizada; originalidade e contribuições para a área; correção de linguagem e respeito às normas técnicas - ABNT.
								<BR>
								Os resultados serão expressos como: <b>Aceito</b>; <b>Aceito com reformulações</b>; <b>Não aceito</b>.
								<BR>
								Para publicação dos textos, os autores deverão encaminhar, após a aprovação do trabalho, Carta de Cessão de Direitos Autorais que se encontra disponível no processo de submissão online (Reol 2.0).
								<BR>
								Será solicitado ao(s) autor(es) a tradução para a língua inglesa do texto aceito.
								<BR>
								Cabe aos autores declarar respeito às <b>normas de ética de pesquisa</b> com seres humanos conforme normas em vigor. A declaração deverá ser expressa por ocasião da submissão do artigo em campo específico incluído na plataforma de submissão.
								<BR>
							</P>
							<BR>
							<b>FORMA E APRESENTAÇÃO DOS ORIGINAIS</b>
							<BR>
							<BR>
							<P>
								Os textos aceitos pela Comissão se enquadram nas categorias:
								<BR>
								<b>- Artigo científico original</b>: oriundo de resultado de pesquisa e sua estrutura deve conter: Introdução, Metodologia, Conclusão, e Referências. O texto deve ser elaborado com, no máximo, 15 páginas e conter até cinco ilustrações quando for o caso;
								<BR>
								<b>- Artigo de Revisão</b>: oriundos de estudos com delineamento definido, consistente,  baseados em pesquisa bibliográfica, catálogos, banco de teses, indexadores, que possam contribuir com o estado da arte contendo até 15 páginas;
								<BR>
								<b>- Resenha</b>: análise interpretativa crítica de uma obra, orientando o leitor quanto às suas características e usos potenciais contendo até três páginas.
								<BR>
								<BR>
								Os trabalhos devem ser apresentados pelo sistema homologado pelo movimento OAI 2.0 de acesso livre à informação desenvolvido pela Pró-Reitoria de Pesquisa e Pós-Graduação da PUCPR (http://www2.pucpr.br/reol/pb/index.php/dialogo?dd99=submit). Contatos poderão ser realizados pelo e-mail: dialogo.educacional@pucpr.br.
								<BR>
								<BR>
								Os textos devem ser digitados em Word for Windows, em fonte Times New Roman, tamanho 12 pt, espaçamento entre linhas de 1,5, formato A4, não devendo ultrapassar 15 páginas para os artigos e cinco páginas para as resenhas, incluindo as referências, ilustrações, figuras, gráficos, quadros e tabelas.
								<BR>
								As ilustrações, figuras, gráficos, etc., devem ser limitadas ao mínimo indispensável (observando que o título também deverá ser indicado no texto onde a ilustração será inserida), identificadas e numeradas consecutivamente em algarismos arábicos. As imagens digitalizadas devem ser enviadas cada uma em arquivo separado, com extensão .jpg ou .tif e resolução mínima de 300 dpi.
								<BR>
								Os quadros e tabelas devem ser limitados ao mínimo indispensável e enviados separadamente do texto (observando que o título também deve ser indicado no texto onde o quadro/tabela será inserido), identificados e numerados consecutivamente em algarismos arábicos. Na montagem das tabelas, seguir as Normas de apresentação tabular, estabelecidas pelo Conselho Nacional de Estatística e publicadas pelo IBGE em 1979. Os quadros e tabelas devem ser enviados cada um em arquivo separado, com extensão doc. ou xls.
								<BR>
								As notas de rodapé são indicadas por algarismos arábicos e restritas ao mínimo indispensável.
								<BR>
							</P>
							<BR>
							No preparo do original, deverá ser observada a seguinte estrutura:
							<BR>
							<BR>
							<b>Cabeçalho</b>
							<BR>
							Título do artigo em português (Inicial em maiúscula, restante em letras minúsculas, a menos que seja nome próprio, em negrito, fonte Times New Roman, tamanho 14 pt, centralizado), subtítulo em letras minúsculas (exceção para nome próprio), em inglês (obrigatório), francês ou espanhol (somente a primeira letra do título em maiúscula, as demais palavras em letras minúsculas – exceção para nome próprio), em itálico, fonte Times New Roman, tamanho 12 pt, centralizado). O título deve conter no máximo 12 palavras, sendo suficientemente específico e descritivo.
							<BR>
							Nome(s) do(s) autor(es) deve(m) estar abaixo do título e apresentar: afiliação institucional/vínculo, cidade, estado, país e e-mail.
							<BR>
							<BR>
							<b>Resumo (Abstract, Resumem)</b>
							<BR>
							O resumo é a apresentação sintetizada dos pontos principais do texto, destacando as considerações emitidas pelo autor, com uso de 100 a 250 palavras. O correspondente abstract deve ser compatível com o texto do resumo.
							<BR>
							Palavras-chave no idioma original e suas correspondentes <i>Keywords</i> e <i>Palabras-clave</i>.  São descritores que identificam o conteúdo do artigo. O número de descritores é de no mínimo três e de, no máximo, cinco.
							<BR>
							<BR>
							<b>Corpo do texto</b>
							<BR>
							<i>Introdução</i>: Contém  o propósito do estudo, de maneira concisa, e descrever quais os avanços que foram alcançados com a pesquisa. Inclui a contextualização do problema por meio de revisão bibliográfica. A introdução não deve incluir dados ou conclusões do trabalho em questão.
							<BR>
							<i>Metodologia</i>: Apresenta a trajetória metodológica em sua abordagem, procedimentos de investigação e análise dos dados. A fundamentação e argumentação teórico metodológica deve ser pertinente ao tema abordado e possibilitar uma análise densa e aprofundada.  A literatura deve expressar referenciais atualizados da área.
							<BR>
							<i>Conclusão ou Considerações finais</i>: Apontam as contribuições ao propósito do texto relacionando-as ao conhecimento já existente, ampliando e aprofundando as discussões. Incluir as citações indispensáveis para embasar o estudo.
							<BR>
							<i>Agradecimentos</i>: Quando pertinentes poderão ser incluídos, devem ser sintéticos e concisos.
							<BR>
							<i>Referências</i>: Somente as citações que figuram no texto devem ser referenciadas. Trabalhos aceitos em fase de publicação devem ser citados acompanhados da expressão <i>“in press”</i>, dados não publicados devem vir acompanhados da expressão <i>“unpublished observations”</i>; neste caso, uma carta do autor deve ser fornecida. As referências no final do trabalho devem ser organizadas e apresentadas em ordem alfabética de acordo com o sobrenome do primeiro autor. Para elaboração das referências, deve ser observada a norma NBR 6023 da ABNT, ago. 2002. Não utilizar abreviações nem siglas dos nomes das instituições. Deve-se evitar ao máximo a citação de citação.
