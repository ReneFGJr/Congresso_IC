									<b>Citações</b>
									<BR>
									<BR>
									As referências devem ser citadas no texto pelo sistema autor-data conforme a norma ABNT, NBR 10520 ago. 2002. Exemplo:
									<BR>
									<b>Quando os autores fazem parte do texto</b>: um autor: Segundo Freitas (2002, p. 524), “.................”; dois autores: De acordo com Junqueira e Souza (2002)...; mais de três autores: Para Prado et al. (2002)...
									<BR>
									<b>Quando os autores não fazem parte do texto</b>: Com o advento da invenção dos tipos móveis por Gutenberg, a informação popularizou-se (BUFREN, 1997).
									<BR>
									<b>Citação de citação</b>: Segundo Silva (1983 apud ABREU, 1999, p. 3) diz ser [...].
									<BR>
									<b>Importante</b>: Deve-se evitar ao máximo a citação de citação.
									<BR>
									As citações com até três linhas devem estar dentro do texto entre aspas duplas; as citações com mais de três linhas devem estar com recuo de 4 cm da margem esquerda, em fonte tamanho 10 pt.
									<BR>
									<BR>
									<BR>
									É importante que, durante a execução do trabalho, o autor consulte a página da revista online (http://www2.pucpr.br/reol/pb/index.php/dialogo?dd99=actual) e verifique a apresentação dos artigos publicados, adotando o mesmo formato. Além de revisar cuidadosamente o trabalho com relação às normas solicitadas: tamanho da fonte em cada item do trabalho, numeração de página, notas em algarismo  arábico, a legenda de tabelas e quadros, formatação da página e dos parágrafos, citação no corpo do texto e referências conforme solicitado e se em português, inglês e/ou espanhol. E, por fim, se todos os autores citados constam nas Referências ao fim do trabalho.
